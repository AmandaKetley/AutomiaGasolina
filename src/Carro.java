public class Carro {
    private float entradaGasolina;
    private float entradaEtanol;

    public Carro() {
    }

    public float getEntradaGasolina() {
        return entradaGasolina;
    }

    public void setEntradaGasolina(float entradaGasolina) {
        this.entradaGasolina = entradaGasolina;
    }

    public float getEntradaEtanol() {
        return entradaEtanol;
    }

    public void setEntradaEtanol(float entradaEtanol) {
        this.entradaEtanol = entradaEtanol;
    }
}
